class HelloOOP {
    private String name;
    private String nim;

    public HelloOOP(String name, String nim) {
        this.name = name;
        this.nim = nim;
    }

    public void greet() {
        System.out.println("Hello World, I am " + name + "-" + nim);
    }
}

public class OOP {
    public static void main(String[] args) {
        HelloOOP student = new HelloOOP("Rafi Kurniawan", "240202878");
        student.greet();
    }
}
